weather = 'thundersnow'
case weather
when 'sunny'
  puts "Bust out those sandals!"
when 'foggy'
  puts "Today's #1 accessory is tea." 
when 'rainy'
  puts "Hope you packed that umbrella"
when 'snowing'
  puts "Call in sick and stay in those pajamas!"
when 'cloudy'
  puts "Today's #1 accessory is existential angst."
when 'thundersnow'
  puts "Batton down the hatches, bundle up, and stay the hell inside!"
end